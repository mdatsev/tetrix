let data = getData()

for(const mino in data)
{
    data[mino] = data[mino]
        .map(rotation => rotation
            .map(row => row
                .map(tile => tile ? "#" : ".")
            .join('')))
        .map(e => e);
}

// console.log(JSON.stringify(data))
for(const mino in data)
{
    data[mino] = data[mino]
        .map(rotation => {
                let tiles = []
                for(let i = 0; i < rotation.length; i++) {
                    for(let j = 0; j < rotation[i].length; j++) {
                        if(rotation[i][j] == '#') 
                            tiles.push([i, j])
                    }
                }
                return tiles
            })
}

